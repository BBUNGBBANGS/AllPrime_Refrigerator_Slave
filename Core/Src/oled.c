static void Clear_TxBuffer(void)
{
    uint16_t idx;
    
    for(idx = 0;idx<100;idx++)
    {
        Tx_OLED_Data[idx] = ' ';
    }
}

static void OLED_Update_Screen(void)
{
    unsigned char cmd_buf[3];
    unsigned char idx;

    for(idx = 0;idx<8;idx++)
    {
        cmd_buf[0] = 0xB0+idx;
        cmd_buf[1] = 0x00;
        cmd_buf[2] = 0x10;
        OLED_Write_Command(cmd_buf,3);
        OLED_Write_Data(&OLED_Buffer[OLED_WIDTH * idx],OLED_WIDTH);
    }

    return;
}

static void OLED_I2C_Write(unsigned char data)
{
    unsigned char tmp;

    Tx_I2C_Data = data;
    Tx_I2C_Enable = 0x08;
    HAL_I2C_Master_Transmit(&OLED_MODULE,0xE0,&Tx_I2C_Enable,1,10);
    HAL_Delay(1);
    HAL_I2C_Master_Transmit(&OLED_MODULE,OLED_ADDRESS,&Tx_I2C_Data,1,10);

    return;
}

